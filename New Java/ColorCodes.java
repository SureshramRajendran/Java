class Color_Codes {
public static void main(String[]args){
char ch;
ch=args[0].charAt(0);
if(ch=='r'){System.out.println("Red");}
else if(ch=='w'){System.out.println("White");}
else if(ch=='o'){System.out.println("Orange");}
else if(ch=='p'){System.out.println("Pink");}
else {System.out.println("Invalid color");}
}
}
